package org.webmaple.common.view;

/**
 * @author lyifee
 * on 2021/4/2
 */
public class BasicDataView {
    private Integer nodeNum;

    private Integer spiderNum;

    private Integer timedJobNum;

    public Integer getNodeNum() {
        return nodeNum;
    }

    public void setNodeNum(Integer nodeNum) {
        this.nodeNum = nodeNum;
    }

    public Integer getSpiderNum() {
        return spiderNum;
    }

    public void setSpiderNum(Integer spiderNum) {
        this.spiderNum = spiderNum;
    }

    public Integer getTimedJobNum() {
        return timedJobNum;
    }

    public void setTimedJobNum(Integer timedJobNum) {
        this.timedJobNum = timedJobNum;
    }
}
